void main() {
  print("Write Program to Swap Values of Two Variables");
  int a;
  a = 286;
  int b;
  b = 279;
  print("Before Swapping");
  print("Value of a = $a");
  print("Value of b = $b");

  a = a + b; // 565
  b = a - b; // 286
  print("After Swapping");
  print("Value of a = $a");
  print("Value of b = $b");
}
